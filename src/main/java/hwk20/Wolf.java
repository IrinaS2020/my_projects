package hwk20;

public class Wolf extends Carnivores {
    public Wolf() {
        System.out.println("\nI AM WOLF!");
    }

    public void eatMeat() {
        System.out.println("Wolfs eat meat!");
    }

    public void walk() {
        System.out.println("I run in the forest");
    }

    public void backbone() {
        System.out.println("I have a backbone!");
    }

    public void breed() {
        System.out.println("I give birth baby wolves!");
    }

    public void breath() {
        System.out.println("I am breathing!");
    }

    public void talk() {
        System.out.println("I talk to the moon!");
    }

    public void alive() {
        System.out.println("I live!");
    }

    public void move() {
        System.out.println("I run and sniff!");
    }

}
