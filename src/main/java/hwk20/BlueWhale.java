package hwk20;

public class BlueWhale {
    public BlueWhale() {
        System.out.println("\nI AM BLUEWHALE!");
    }

    public void eatMeat() {
        System.out.println("Whales don't eat meat!");
    }

    public void walk() {
        System.out.println("Whales cannot walk!");
    }

    public void backbone() {
        System.out.println("I have a backbone!");
    }

    public void breed() {
        System.out.println("BlueWhale gives birth to baby whales");
    }

    public void breath() {
        System.out.println("I need to breath sometimes");
    }

    public void talk() {
        System.out.println("I make some underwater sounds!");
    }

    public void alive() {
        System.out.println("I am alive!");
    }

    public void move() {
        System.out.println("I can swim!");
    }

}
